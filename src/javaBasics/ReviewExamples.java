package javaBasics;

public class ReviewExamples {
	public static void main(String[] args) {

		// increment - is increasing the value of the variable and assigning in back to the variable
		
		int x = 10;
		
		System.out.println(++x);
		
		System.out.println(x);
		
		System.out.println(x + 1);
		
		System.out.println(x);
		
	}
}
