package phoneix_Java_Basic;

public class EscapeSequenceExample {
	public static void main(String[] args) {
		
		
		String test = "\tthis is a \n\"test\" \nstring";
		
		System.out.println(test);
		
		
		//   the escape sequence is used in special cases
		//   \n means go to the next line 
		//   \t means indent the line 
		// 	 \" will just print the "
		
		
		
	}
}
